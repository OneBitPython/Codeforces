void solve()
{
    int n,k;
    cin >> n >> k;
    vector<pair<int,int>> arr(n);
    for(int i = 0;i<n;++i) cin >> arr[i].first;
    for(int i = 0;i<n;++i) cin >> arr[i].second;
    sort(all(arr), [&](auto one, auto two){
        if(one.first == two.first) return one.second > two.second;
        return one.first < two.first;
    });
    int ans=  k;
    for(int i = 0;i<n;++i){
        if(arr[i].first <= ans){
            ans+=arr[i].second;
        }
    }
    cout << ans << endl;
}

