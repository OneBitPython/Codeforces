vector<pair<int,int>>coprimes;
void preprocess(){
    for(int i = 1;i<=1000;++i){
        for(int j = i;j<=1000;++j){
            if(__gcd(i,j)==1)coprimes.pb({i,j});
        }
    }
}

void solve()
{
    int n;
    cin >> n;
    map<int,int>mx;
    for(int i = 1;i<=n;++i){
        int v;
        cin >> v;
        mx[v] = max(mx[v], i);
    }
    int res = -1;
    for(auto x : coprimes){
        if(mx.count(x.first) && mx.count(x.second)){
            res = max(res, mx[x.first]+mx[x.second]);
        }
    }
    cout << res << endl;
}   

