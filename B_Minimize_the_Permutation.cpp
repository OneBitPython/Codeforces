string bin(long n){
    long i;
    string val = "0";
    for (i = 1 << 30; i > 0; i = i / 2)
    {
      if((n & i) != 0)
      {
        val+="1";
      }
      else
      {
        val+="0";
      }
    }
    while(val.front()=='0'){
        val.erase(0,1);
    }
    return val;
}




void solve()
{
    int n;
    cin >> n;
    vector<int>a(n);
    for(int &u : a)cin >> u;
    int op = 0;
    int find = 0;
    int taken_till = 0;
    int k = 0;
    while(find < n){

        int pos = find;
        // find min value in the range [find+1, n] which is lesser than a[find] and can be swapped to a[find] in the required operations
        int tmp_op = op;
        int min_ = a[find];
        if(tmp_op>=n-1)break;
        for(int i = find+1;i<n;++i){
            if(tmp_op >= n-1)break;
            if(a[i] < min_){
                min_ = a[i];
                pos = i;
            }
            tmp_op++;
        }
        for(int j = pos;j>=find+1;--j){
            swap(a[j],a[j-1]);
            op++;
        }

        find = max(find+1, pos);
    }
    for(auto x: a)cout << x << ' ' ;
    cout << endl;
}   

