void solve()
{
    int n,k;
    cin >> n >> k;
    vector<int>a(n+1),grp(n+1);
    int c = 1;
    for(int i = 1;i<=n;i+=k){
        int l = i, r = min(n,i+k-1);
        int kk = r-l+1;
        int val = (kk+1)/2 + i-1;
        int pos = i;
        int pp = val;
        while(val>=i){
            a[pos] = val;
            val--;
            pos++;
        }
        val = r;
        while(val > pp){
            a[pos] =val;
            val--;
            pos++;
        }
        for(int j = l;j<=r;++j)grp[j] = c;
        c++;
    }
    for(int i= 1;i<=n;++i)cout << a[i] << ' ';
    cout << endl;
    cout << grp[n] << endl;
    for(int i= 1;i<=n;++i)cout << grp[i] << ' ';
    cout << endl;
}   

