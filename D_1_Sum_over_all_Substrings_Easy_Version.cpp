void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    int res = 0;
    s = "0"+s;
    for(int i = 1;i<=n;++i){
        vector<vector<int>>dp(n+1, vector<int>(2));
        dp[i][1] = 1;
        if(s[i]=='1')dp[i][0] = 1e18,res++;
        for(int j = i+1;j<=n;++j){
            if(s[j]=='1'){
                dp[j][0] = dp[j-1][1];
                dp[j][1] = min({dp[j-1][0],dp[j-1][1], dp[j-2][0],dp[j-2][1]})+1;
            }else{
                dp[j][0] = min(dp[j-1][0],dp[j-1][1]);
                dp[j][1] = min({dp[j-1][0],dp[j-1][1], dp[j-2][0],dp[j-2][1]})+1;

            }
            res+=min(dp[j][0],dp[j][1]);
        }
    }
    cout << res << endl;
}   

