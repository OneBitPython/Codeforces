void solve()
{
    int n;
    cin >> n;
    vector<int>a(n+1);
    for(int i = 1;i<=n;++i)cin >> a[i];
    vector<vector<int>>dp;
    // dp[i] = {start,end,value}
    dp.pb({0,1,0});
    dp.pb({2,3,1});
    dp.pb({4,5,2});
    int curr = 6;
    int start = 6;
    for(int i = 0;i<=100;++i){
        if(curr > (int)(1e9))break;
        dp.pb({curr,curr+start-1,0});
        curr+=start;
        dp.pb({curr,curr+(2*start)-1,1});
        curr+=(2*start);
        dp.pb({curr,curr+(2*start)-1,2});
        curr+=(2*start);
        start*=6;
    }
    int res = 0;
    for(int i = 1;i<=n;++i){
        for(auto x : dp){
            if(a[i]>=x[0] && a[i] <= x[1]){
                res^=x[2];
                break;
            }
        }
    }
    if(res==0)cout << "Miku" << endl;
    else cout << "Nino" << endl;
}   

