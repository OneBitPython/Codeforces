void solve()
{
    string l,r;
    cin >> l >> r;
    int c = (int)(r.size())-(int)(l.size());
    int res = 0;
    if(c==0){
        for(int i = 0;i<l.size();++i){
            if(l[i] != r[i]){
                res+=abs((l[i]-'0')-(r[i]-'0'));
                res+=(9*((int)(r.size())-i-1));
                break;
            }
        }
        cout << res << endl;
        return;
    }
    int get = 0;
    for(int j = 0;j<c;++j)get+=(r[j]-'0');
    res += ((r[0]-'0'));
    res+=(9*(c-1));
    res = max(res, get);
    res+=(9*((int)(r.size())-c));
    cout << res << endl;
}   

