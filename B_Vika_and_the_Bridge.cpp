void solve()
{
    int n,k;
    cin >> n >> k;
    vector<int>a(n+1);
    vector<vector<int>>col(k+1,vector<int>(1,0));
    for(int i = 1;i<=n;++i){
        cin >> a[i];
        col[a[i]].pb(i);
    }
    int res = 1e18;
    for(int i = 1;i<=k;++i){
        if(col[i].size()==1)continue;
        col[i].pb(n+1);
        vector<int>vals;
        for(int j = 1;j<(int)(col[i].size());++j){
            vals.pb(col[i][j]-col[i][j-1]-1);
        }
        sort(all(vals),greater<int>());
        int x = vals[0];
        vals.erase(vals.begin());
        vals.pb(x/2);
        sort(all(vals),greater<int>());
        res = min(res, vals[0]);
    }
    cout << res << endl;
}   

