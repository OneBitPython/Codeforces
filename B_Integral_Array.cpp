void solve()
{
    int n,c;
    cin >> n >> c;
    vector<int>a(n+1);
    vector<int>cnt(c+1);
    for(int i = 1;i<=n;++i)cin >> a[i],cnt[a[i]]=1;
    set<int>k(a.begin()+1, a.end());
    vector<int>pref(c+1);
    for(int i = 1;i<=c;++i)pref[i] = pref[i-1]+cnt[i];
    for(auto x : k){
        for(int i = x;i<=c;i+=x){
            int e = min(c,i+x-1);
            int cont = pref[e]-pref[i-1];
            if(cont > 0 && cnt[i/x] == 0){
                cout << "No" << endl;
                return;
            }
        }
    }
    cout << "Yes" << endl;
}   

