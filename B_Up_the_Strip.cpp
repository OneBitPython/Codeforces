int n,m;
int ma(int a, int b){
    return (a+b)%m;
}
int ms(int a, int b){
    return ((((a%m)-(b%m))%m)+ m)%m;
}

void solve()
{
    cin >> n >> m;
    vector<int>dp(n+1),suff(n+2);
    dp[n] = 1;
    suff[n] = 1;
    for(int i = n-1;i>=1;--i){
        dp[i] = ma(dp[i], suff[i+1]);
        int add = 0;
        for(int j = 2;;++j){    
            if(i*j > n)break;
            int till = min(n,(i*j)+j-1);
            add = ma(add, ms(suff[i*j],suff[till+1]));
        }
        
        dp[i] = ma(dp[i],add);
        suff[i] = ma(suff[i+1],dp[i]);
    }
    cout << dp[1]%m << endl;
}   

