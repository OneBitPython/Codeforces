void solve()
{
    string n;
    cin >> n;
    
    vector<int>p; // primes
    for(int i = 0;i<=60;++i){
        p.pb((1ll<<i));
    }
    int res = 1e9;
    for(auto x : p){
        int changes = 0;
        string c;

        // vector<int>occ2(10);
        // print(x);
        while(x!=0){
            c+=((x%10)+'0');
            x/=10;

        }
        reverse(all(c));
        int taken = 0, check = 0; // convert n into c
        // check how many characters of n to take in c
        //1052
        //1578
        //
        int remove = 0;
        for(int i = 0;i<n.size();++i){
            if(check >= c.size()){remove += n.size()-i;break;}
            if(c[check] == n[i]){taken++;check++;}
            else remove++;
        }
        changes+=(c.size()-taken);
        changes+=remove;
        res = min(res, changes);

    }
    //1
    // 67194767
    // 16777216   
    cout << res << endl;
}

