void solve()
{
    int x,y;
    cin >> x >> y;
    int st1 =0, st2 = 0;
    for(int j = 60;j>=0;--j){
        if(x&(1ll<<j)){
            st1 = j;
            break;
        }
    }
    for(int j = 60;j>=0;--j){
        if(y&(1ll<<j)){
            st2 = j;
            break;
        }
    }
    if((x>>st2)&1){
        cout << 1 << endl << x << ' ' << y << endl;
        return;
    }
    bool got =0;
    for(int j = st2+1;j<st1;++j){
        if(x&(1ll<<j))got = 1;
    }
    if(!got)cout << -1 << endl;
    else cout << 2 << endl << x << ' ' << y+(1ll<<st1) << ' ' << y << endl;
}   

