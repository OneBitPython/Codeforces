vector<int>g(2001);
void pre(){
    for(int i = 1;i<=2000;++i){
        set<int>c;
        for(int j = 1;j<i;++j){
            if(j!=(i-j))c.insert(g[j]^g[i-j]);
        }
        for(int j = 0;j<=2100;++j){
            if(c.count(j)==0){
                g[i] = j;
                break;
            }
        }
    }

}

void solve()
{
    int n;
    cin >> n;
    if(n>2000){
        cout << "first" << endl;
        return;
    }
    if(g[n] == 0)cout << "second" << endl;
    else cout << "first" << endl;
}   

