bool pos(double x){
    double add = 0,req = 0;
    for(int i = 0;i<n;++i){
        if(a[i]>=x)add+=a[i]-x;
        else req += x-a[i];
    }
    double get = (add-((add*k)/100));
    return req < get;
}

