string bin(long n){
    long i;
    string val = "0";
    for (i = 1 << 30; i > 0; i = i / 2)
    {
      if((n & i) != 0)
      {
        val+="1";
      }
      else
      {
        val+="0";
      }
    }
    while(val.front()=='0'){
        val.erase(0,1);
    }
    return val;
}


int N = 2e5+5;
ll fact[(int)(2e5+5)];
ll invFact[(int)(2e5+5)];
int mod = 1e9+7;
ll fast_pow(ll a, ll p)
{
    ll res = 1;
    while (p)
    {
        if (p % 2 == 0)
        {
            a = (a * a) % mod;
            p /= 2;
        }
        else
        {
            res = (res * a) % mod;
            p--;
        }
    }
    return res;
}

ll ncr(int n, int k)
{
    if (k > n)
    {
        return 0;
    }
    return fact[n] * invFact[k] % mod * invFact[n - k] % mod;
}

void solve()
{
    int n,m,k;
    cin >> n>> m >> k;
    vector<int>a(n);
    for(int &u : a)cin >>u;
    sort(all(a));
    vector<pair<int,int>>ranges;
    int res = 0;
    int min_reached = n;
    for(int i = n-1;i>=0;--i){
        int idx = lower_bound(all(a), a[i]-k)-a.begin();
        int cnt = i-idx+1;
        if(cnt < m)continue;
        if(idx < min_reached){
            min_reached = idx;
            ranges.pb({idx, i});
            // nCr = n!/(n-r)!r! = cnt!/(cnt-m)!m!
            res=((res%mod)+(ncr(cnt, m)%mod)%mod);
        }
    }
    sort(all(ranges));
    for(int i = 0;i<(int)(ranges.size()-1);++i){
        int cnt = ranges[i].second - ranges[i+1].first + 1;
        if(cnt<m)continue;
        res = (((res%mod)-(ncr(cnt, m)%mod))+mod) % mod;
    }
    cout << res << endl;
}   

