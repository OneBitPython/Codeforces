void solve()
{
    int n;
    cin >> n;
    if(n%2==0){
        int curr = 1;
        cout << "YES" << endl;
        for(int i = 0;i<n;++i){
            cout << curr << ' ';
            curr = -curr;
        }
        cout << endl;
        return;
    }
    if(n==3){
        cout << "NO" << endl;
        return;
    }
    int val = (n-5)/2;
    int first = -1-val;
    int second = 2+val;
    int curr = first;
    cout << "YES" << endl;
    for(int i = 0;i<n;++i){
        cout << curr << ' ';
        if(curr==second)curr = first;
        else curr = second;
    }
    cout << endl;
}   

