void solve()
{
    int n,m;
    cin >> n >> m;
    vector<int>dp(n+1);
    for(int i = 1;i<=n;++i)dp[i] = (n/i)*((n/i)-1)/2;
    for(int i = n;i>=1;--i){
        for(int j = i+i;j<=n;j+=i)dp[i] -= dp[j];
    }
    int res = 0;
    int mm = m;
    for(int i = n;i>=2;--i){
        // weight of edges is i
        // take some multiple of i edges
        if(m==0)continue;
        int v1 = dp[i]-(dp[i]%(i-1));
        int x = (m/(i-1));
        
        if(v1 > (x*(i-1)))m-=((i-1)*x),res+=x;
        else m-=v1,res+=(v1/(i-1));
    }
    if(m!=0)cout << -1 << endl;
    else cout << res+mm << endl;
}   

