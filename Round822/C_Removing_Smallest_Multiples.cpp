void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    vector<int>taken(n+1,1e18);
    vector<int>need;
    for(int i = 0;i<n;++i){
        if(s[i] == '0')need.pb(i+1);
    }
    int cost = 0;
    for(auto x : need){
        for(int i = x;i<=n;i+=x){
            if(s[i-1]=='1')break;
            taken[i] = min(taken[i], x);
        }
    }    
    for(auto x : need)cost+=taken[x];
    cout << cost << endl;
}   

